# 깔끔한 출력을 위한 자릿수 또는 소수점 결정.

print("%d" %123)
print("%5d" %123)
print("%05d\n" %123)

print("%f" %123.46)
print("%7.1f" %123.46)
print("%7.3f\n" %123.46)

print("%s" %"Python")
print("%10s" %"Python")


print("\n줄바꿈\n연습")
print("\t탭키\t연습")
print("글자가\"강조\"되는 효과1")
print("글자가\'강조\'되는 효과2")
print("\\\\\\ 역슬레시 세 개 출력")
print(r"\n \t \" \\를 그대로 출력")
